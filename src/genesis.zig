// ── Genesis Result ────────────────────────────────────────────────────

pub const GenesisResult = struct {
    chain_id: u64,
    genesis_hash: primitives.Hash.Hash,
    coinbase: primitives.Address,
    managed_accounts: []const DevAccount,
};

// ── Genesis Block Construction ────────────────────────────────────────

pub fn createGenesisBlock(allocator: std.mem.Allocator) !primitives.Block.Block {
    const header = primitives.BlockHeader.BlockHeader{
        .number = 0,
        .gas_limit = DEFAULT_GAS_LIMIT,
        .base_fee_per_gas = DEFAULT_BASE_FEE,
        .timestamp = @intCast(std.time.timestamp()),
        .beneficiary = DEV_ACCOUNTS[0].address,
        .difficulty = 0,
        .ommers_hash = primitives.BlockHeader.EMPTY_OMMERS_HASH,
        .transactions_root = primitives.BlockHeader.EMPTY_TRANSACTIONS_ROOT,
        .receipts_root = primitives.BlockHeader.EMPTY_RECEIPTS_ROOT,
        // parent_hash defaults to Hash.ZERO — correct for genesis
        // mix_hash defaults to Hash.ZERO — correct for post-merge
        // nonce defaults to zeros — correct for post-merge
    };
    const body = primitives.BlockBody.init();
    return primitives.Block.from(&header, &body, allocator);
}

// ── Genesis State Initialization ──────────────────────────────────────

pub fn initGenesisState(state: *state_manager.StateManager) !void {
    for (&DEV_ACCOUNTS) |*account| {
        try state.setBalance(account.address, DEV_BALANCE);
    }
}

// ── Full Genesis Initialization ───────────────────────────────────────

pub fn initGenesis(
    allocator: std.mem.Allocator,
    db: *@import("database/root.zig").Database,
    chain: *blockchain_mod.Blockchain,
) !GenesisResult {
    // 1. Fund all dev accounts in StateManager
    try initGenesisState(&db.state);

    // 2. Create genesis block with proper header fields
    const genesis_block = try createGenesisBlock(allocator);

    // 3. Store in blockchain and set as canonical head
    try chain.putBlock(genesis_block);
    try chain.setCanonicalHead(genesis_block.hash);

    // 4. Record genesis hash in block_hashes (for BLOCKHASH opcode)
    try db.block_hashes.put(allocator, 0, genesis_block.hash);

    return .{
        .chain_id = CHAIN_ID,
        .genesis_hash = genesis_block.hash,
        .coinbase = DEV_ACCOUNTS[0].address,
        .managed_accounts = &DEV_ACCOUNTS,
    };
}

// ── Startup Banner ────────────────────────────────────────────────────

pub fn printBanner(writer: anytype) !void {
    try writer.writeAll("\nzevm - Ethereum local node\n");
    try writer.print("Chain ID: {d}\n\n", .{CHAIN_ID});

    try writer.writeAll("Available Accounts\n==================\n");
    for (&DEV_ACCOUNTS, 0..) |*account, i| {
        try writer.print("({d}) 0x{s} (10000 ETH)\n", .{ i, std.fmt.fmtSliceHexLower(&account.address.bytes) });
    }

    try writer.writeAll("\nPrivate Keys\n==================\n");
    for (&DEV_ACCOUNTS, 0..) |*account, i| {
        try writer.print("({d}) 0x{s}\n", .{ i, std.fmt.fmtSliceHexLower(&account.private_key) });
    }

    try writer.print(
        \\
        \\Wallet
        \\==================
        \\Mnemonic: {s}
        \\Derivation path: {s}
        \\
        \\Base Fee: {d} (1 gwei)
        \\Gas Limit: {d}
        \\
        \\
    , .{ MNEMONIC, DERIVATION_PATH, @as(u64, @intCast(DEFAULT_BASE_FEE)), DEFAULT_GAS_LIMIT });
}
